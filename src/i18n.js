  function normalizeUiLanguage(value) {
    const raw = String(value || "").trim().toLowerCase();
    if (raw === "en" || raw.startsWith("en-") || raw === "english") {
      return "en";
    }
    if (
      raw === "zh" ||
      raw === "zh-cn" ||
      raw === "zh-hans" ||
      raw.includes("中文") ||
      raw.includes("简体") ||
      raw.includes("chinese")
    ) {
      return "zh-CN";
    }
    return DEFAULT_UI_LANGUAGE;
  }

  function create(settingsOrLanguage) {
    const language = normalizeUiLanguage(
      typeof settingsOrLanguage === "string" ? settingsOrLanguage : settingsOrLanguage?.uiLanguage
    );
    return {
      language,
      locale: language === "en" ? "en-US" : "zh-CN",
      t(key, variables = {}) {
        const template = MESSAGES[language]?.[key] || MESSAGES[DEFAULT_UI_LANGUAGE]?.[key] || key;
        return template.replace(/\{(\w+)\}/g, (match, name) => {
          return variables[name] == null ? match : String(variables[name]);
        });
      }
    };
  }

  global.VCS_I18N = {
    DEFAULT_UI_LANGUAGE,
    SUPPORTED_UI_LANGUAGES,
    normalizeUiLanguage,
    create,
    messages: MESSAGES
  };
